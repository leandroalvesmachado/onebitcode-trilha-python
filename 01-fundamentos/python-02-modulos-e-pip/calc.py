"""Módulo Calculadora."""


def sum(a, b):
    """Retorna a soma de dois números."""
    result = a + b
    return result


def sub(a, b):
    """Retorna a subtração de dois números."""
    result = a - b
    return result


def mult(a, b):
    """Retorna a multiplicação de dois números."""
    result = a * b
    return result


def div(a, b):
    """Retorna a divisão de dois números."""
    result = a / b
    return result
